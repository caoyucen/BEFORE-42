int		ft_iterative_factorial(int nb)
{
	int n;
	int i;

	n = nb;
	if (n < 0 || n > 12)
	{
		return (0);
	}
	else
	{
		i = 1;
		while (n > 0)
		{
			i = n * i;
			n--;
		}
		return (i);
	}
}
