int		ft_is_prime(int nb)
{
	int a;

	if (nb < 2)
		return (0);
	if (nb % 2 == 0)
		return (0);
	a = 3;
	while (a < nb / 2)
	{
		if(nb % a == 0)
		{
			return (0);
			a = a + 2;
		}
	}
	return (1);
}
