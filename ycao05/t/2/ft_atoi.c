int		ft_atoi(char *str)
{
	int		neg;
	int		nb;

	neg = 0;
	while (*str == ' ' || *str == '\n' || *str == '\t'
		|| *str == '\v' || *str == '\f' || *str == '\r')
		str++;
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
			neg = 1;
		str++;
	}
	if (*str < '0' || *str > '9')
		return (0);
	nb = 0;
	while (*str >= '0' && *str <= '9')
	{
		nb = nb * 10 + (*str - '0');
		str++;
	}
	if (neg == 1)
		return (-nb);
	else
		return (nb);
}
