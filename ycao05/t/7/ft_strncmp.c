int		ft_strcmp(char *a, char *b, unsigned int n)
{
	int		i;

	i = 0;
	while (a[i] != '\0' || b[i] != '\0' && (i < n))
	{
		if (a[i] > b[i])
			return (1);
		if (a[i] < b[i])
			return (-1);
		i++;
	}
	return (0);
}
