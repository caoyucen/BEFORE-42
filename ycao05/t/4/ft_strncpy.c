char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int		i;
	int		j;

	i = 0;
	j = n;
	while (i < n)
	{
		*dest = *src;
		src++;
		dest++;
		i++;
	}
	*dest = '\0';
	while (j >= 0)
	{
		dest--;
		j--;
	}
	return (dest);
}
