#include <stdlib.h>

int *ft_map(int *tab, int length, int(*f)(int))
{
	int *r;
	int i;

	r = (int*)malloc(sizeof(int) * length);
	i = 0;
	while (i < length)
	{
		r[i] = f(tab[i]);
		i++;
	}
	return (r);
}
